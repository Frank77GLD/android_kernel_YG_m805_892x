/**
 * Cleanup the PCD portion of the driver.
 */
void dwc_usb3_pcd_remove(dwc_usb3_device_t *dev)
{
	dwc_debug1(dev, "%s()\n", __func__);

	if (dev->pme_thread) {
		kthread_stop(dev->pme_thread);
		dev->pme_thread = NULL;
	}

	dwc_usb3_core_device_remove(dev);
}
